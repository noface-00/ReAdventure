    private MigLayout layout;
    private Panel_cover cover;
    private Login_Register loginANDRegister;
    private boolean isLogin;
    private final double addSize =  30;
    private final double coverSize = 40;
    private final double loginSize = 60;
    private DecimalFormat df;
    
    public UI() {
        initComponents();
        init();
    }
    
    private void init(){
        DecimalFormatSymbols symbols = new DecimalFormatSymbols();
        symbols.setDecimalSeparator('.');
        df = new DecimalFormat("##0.###", symbols);
        
        layout = new MigLayout("fill, insets 0, debug");
        cover = new Panel_cover();
        loginANDRegister = new Login_Register();
        TimingTarget target = new TimingTargetAdapter() {
            @Override
            public void timingEvent(float fraction){
                double fractionCover;
                double fractionLogin;
                double size = coverSize;
                if(fraction <= 0.5f){
                    size+= fraction * size;
                }else{
                    size += addSize - fraction * addSize;
                }
                if(isLogin){
                    fractionCover = 1f - fraction;
                    fractionLogin = fraction;
                }else{
                   fractionCover = fraction; 
                   fractionLogin = 1f - fraction;
                }
                fractionCover = Double.valueOf(df.format(fractionCover));
                fractionLogin = Double.valueOf(df.format(fractionLogin));
                layout.setComponentConstraints(cover, "width " + size +"%, pos" + fractionCover + "al 0 n 100%");
                layout.setComponentConstraints(loginANDRegister, "width " + loginSize +"%, pos" + fractionLogin + "al 0 n 100%");
                main.revalidate();
            }
            @Override
            public void end() {
                isLogin =! isLogin;
            }
        };
        Animator animator = new Animator(1000, target);
        animator.setAcceleration(0.5f);
        animator.setDeceleration(0.5f);
        animator.setResolution(0); //Suavizado de animacion
        main.setLayout(layout);
        main.add(cover, "width " + coverSize+ "%, pos 0al 0 n 100%");
        main.add(loginANDRegister, "width " + loginSize + "%, pos 1al 0 n 100%");
        cover.addEvent(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if(!animator.isRunning()){
                    animator.start();
                }
            }
        })   ;
        
        
    }
